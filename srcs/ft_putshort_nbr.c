#include "ft_printf.h"

void	ft_putshort_nbr(short n)
{
	if (n == -32768)
	{
		ft_putstr("-32768");
		return ;
	}
	else
	{
		if (n < 0)
		{
			n = -n;
			ft_putchar('-');
		}
		if (n >= 10)
			ft_putshort_nbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
}
