#include "ft_printf.h"

void	ft_puthex(unsigned long nbr)
{
	if (nbr >= 16)
		ft_puthex(nbr / 16);
	if ((nbr % 16) >= 10 && (nbr % 16) <= 15)
		ft_putchar((nbr % 16) - 10 + 'a');
	else if ((nbr % 16) < 10)
		ft_putchar((nbr % 16) + '0');
}
