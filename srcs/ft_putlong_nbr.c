#include "ft_printf.h"

void	ft_putlong_nbr(long nbr)
{
	if (nbr == -9223372036854775807 - 1)
	{
		ft_putstr("-9223372036854775808");
		return ;
	}
	else
	{
		if (nbr < 0)
		{
			nbr = -nbr;
			ft_putchar('-');
		}
		if (nbr >= 10)
			ft_putlong_nbr(nbr / 10);
		ft_putchar(nbr % 10 + '0');
	}
}
